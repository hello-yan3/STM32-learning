void  DEBUG_USART_IRQHandler(void)
{
  uint8_t ch=0; 
  
	/*UART_FLAG_RXNE 接收数据寄存器非空标志位 表示接收到数据*/
	if(__HAL_UART_GET_FLAG( &UartHandle, UART_FLAG_RXNE ) != RESET)
	{		
		// 直接读取DR寄存器数据，之后将数据写入DR寄存器，发送数据
		// 每次只能接收并发送一个数据，之后在此进入中断，重复该过程
		// 由于直接读取寄存器速度更快，此过程时间快于串口接收时间，因此可以实现转发多个数据效果
    ch=( uint16_t)READ_REG(UartHandle.Instance->DR);
    WRITE_REG(UartHandle.Instance->DR,ch); 
		
		// 读取接收到的数据，并将其发送
		// 每次只能接收并发送一个数据，之后在此进入中断，重复该过程
		// 库函数执行速度慢，因此，接收少量数据可以，接收大量数据时将会出错
//		HAL_UART_Receive(&UartHandle, &ch, 1, 10);
//		HAL_UART_Transmit(&UartHandle,&ch ,1, 10);
		
		// 此标志位读取后自动清除，因此不需要手动清除
	}
}
